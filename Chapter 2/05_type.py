"In Python, there are several built-in data types that you can use to store different kinds of data. Here are some of the most common data types:"
# 1. int (integer): This data type is used to store whole numbers without a decimal point. Example: 1, -5, 100
# 2. float (floating-point number): This data type is used to store numbers with
# a decimal point. Example: 3.14, -0.001, 2.0
# 3. str (string): This data type is used to store sequences of characters, such as words or sentences. Example: "Hello, World!", 'Python is great'
# 4. bool (boolean): This data type is used to store one of two values: True or False. Example: True, False
# 5. list: This data type is used to store an ordered collection of items, which can be of different types. Example: [1, 2.5, "Hello", True]
# 6. tuple: This data type is similar to a list, but it is immutable (cannot be changed after creation). Example: (1, 2.5, "Hello", True)
# 7. dict (dictionary): This data type is used to store key-value pairs. Example: {"name": "Anshu", "age": 25, "is_student": True}
# 8. set: This data type is used to store an unordered collection of unique items. Example: {1, 2, 3, 4}
# 9. NoneType: This data type represents the absence of a value or a null value. Example: None

