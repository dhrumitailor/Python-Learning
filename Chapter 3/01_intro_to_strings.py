# single ' , double " , triple ''' or """ can be used to create a string
name = 'Anshu'
name = "Anshu"
name = '''Anshu'''
name = """Anshu"""
# to print a single ' or double " in a string we can use the other one to create the string
name = "Anshu's"
print(name)
name = 'Anshu said "Hello"'
print(name)


# strings are immutable in python which means we cannot change the value of a string after it is created


name = "Anshu"
length = len(name) # length of the string
print(length)

