name = "Anshu"

print(len(name))  # Length of the string
print(name.upper())  # Convert to uppercase
print(name.lower())  # Convert to lowercase
print(name.title())  # Convert to title case
print(name[0:3])  # Substring from index 0 to 2
print(name[-1])  # Last character
print(name[-3:])  # Substring from index -3 to the end
print(name[::2])  # Every second character
print(name[::-1])  # Reverse the string
print(name.count("s"))  # Count occurrences of 's'
print(name.index("s"))  # Find the index of 's'
print(name.islower())  # Check if all characters are lowercase
print(name.isupper())  # Check if all characters are uppercase
print(name.isalnum())  # Check if all characters are alphanumeric
print(name.isnumeric())  # Check if all characters are numeric
print(name.capitalize())  # Capitalize the first character
print(name[0])  # First character
print(name[1:4])  # Substring from index 1 to 3
print(name.startswith("A"))  # Check if it starts with 'A'
print(name.endswith("u"))  # Check if it ends with 'u'
print(name.replace("Anshu", "Anshul"))  # Replace substring
print(name.split("s"))  # Split the string by 's'
print(name.find("s"))  # Find the index of 's'
print(name.isalpha())  # Check if all characters are alphabetic
print(name.isdigit())  # Check if all characters are digits
print(name.strip())  # Remove leading and trailing whitespace
print(name.center(20, "*"))  # Center the string with '*' padding

